gir =input("Lütfen sayı giriniz: ")
gir=int(gir)
if gir>0:
    print("Pozitif seçim.")
elif gir==0:
    print("Nötr seçim.")
elif gir==19:
    print("Sevgili rakam.")
else:
    print("Negatif seçim olmalı.")

